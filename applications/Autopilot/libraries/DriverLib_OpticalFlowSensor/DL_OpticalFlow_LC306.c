flow_data  Flow306;
void lc306_flow_receive(uint8_t data)  //摄像头位置朝向机头
{
    static uint8_t data_len = 0;
    static uint8_t state = 0;

    if(state==0&&data==0xFE)    //
    {
        state=1;
    }
    else if(state==1&&data==0x0A)   //
    {
        state=2;
        data_len = 12;
        Flow306.dataLen = 0;
    }
    else if(state==2&&data_len>0)
    {
        data_len--;
        Flow306.data[Flow306.dataLen++]=data;
        if(data_len==0)
        state = 3;
    }
    else if(state==3 && Flow306.data[11]==0x55)
    {
        state=0;
        Flow306.sonar_timestamp = Flow306.data[5]<<8|Flow306.data[4];    //  20800us
        Flow306.qual = Flow306.data[8];
        if (Flow306.qual==0xF5) {
            Flow306.raw_x =  ((int16_t)(Flow306.data[1]<<8)|Flow306.data[0]);   //
            Flow306.raw_y = -((int16_t)(Flow306.data[3]<<8)|Flow306.data[2]);
        } else {
            Flow306.raw_x = 0;   //
            Flow306.raw_y = 0;
        }
    }
    else
    {
        state=0;
    }

}



